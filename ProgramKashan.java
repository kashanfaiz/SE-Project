import java.util.*;
public class ProgramKashan {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int button = sc.nextInt();
        
        
        if(button == 1){
            System.out.println("KAshan");
        }
        else{
            if(button == 2){
                System.out.println("zeeshan");
            }
            else{
                if(button == 3){
                    System.out.println("Qalab");
                }
                else{
                    System.out.println("jumman");
                }
            }
            
        }
    } 
}
    